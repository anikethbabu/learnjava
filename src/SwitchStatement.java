public class SwitchStatement {
    public static void main(String[] args) {
    String month = "january";
    switch(month.toLowerCase()){
        case "january":
            System.out.println("Jan");
            break;
        case "june":
            System.out.println("June");
            break;
        default:
            System.out.println("Not Sure");

    }
    }

}
